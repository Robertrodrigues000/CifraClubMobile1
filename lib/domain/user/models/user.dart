class User {
  final String? name;
  final String? email;
  final String? avatarUrl;
  final String? city;
  final String? stateInitials;
  final String? state;
  final String? country;
  final int? id;

  const User({
    this.name,
    this.email,
    this.avatarUrl,
    this.city,
    this.state,
    this.stateInitials,
    this.country,
    this.id,
  });
}
